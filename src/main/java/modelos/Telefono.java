package modelos;

public class Telefono {
    private String name;
    private String sku;
    private String descripcion;
    private double precio;

    // Getters
    public String getName() {
        return name;
    }

    public String getSku() {
        return sku;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public double getPrecio() {
        return precio;
    }
}
