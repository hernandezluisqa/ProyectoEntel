package modelos;

public class Accesorio {
    private String sku;
    private String type;
    private String name;
    private String description;
    private double price;

    // Getters
    public String getSku() {
        return sku;
    }

    public String getType() {
        return type;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public double getPrice() {
        return price;
    }

}